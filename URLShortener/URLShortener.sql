 * URL				NOT NULL 		VARCHAR2(500)
 * EXPIRATION_DATE	NOT_NULL		DATE
 */

CREATE SEQUENCE URL_SHORTENER_LOOKUP_SEQ;

CREATE TABLE HFOWNER.URL_SHORTENER_LOOKUP
(
URL_SHORTENER_LOOKUP_ID NUMBER(12) CONSTRAINT URL_SHORTENER_LOOKUP_PK PRIMARY KEY USING INDEX TABLESPACE HFINDEX,
HASH VARCHAR2(6) NOT NULL ENABLE,
URL VARCHAR2(500) NOT NULL ENABLE,
EXPIRATION_DATE DATE NOT NULL ENABLE
);

CREATE UNIQUE INDEX URL_SHORTENER_LOOKUP_INDEX ON URL_SHORTENER_LOOKUP(HASH) TABLESPACE HFINDEX;

COMMENT ON TABLE  HFOWNER.URL_SHORTENER_LOOKUP IS 'New table to hold the short url generated by the URL Shortener.';
COMMENT ON COLUMN  HFOWNER.URL_SHORTENER_LOOKUP.HASH IS 'The Short URL';
COMMENT ON COLUMN  HFOWNER.URL_SHORTENER_LOOKUP.URL IS 'The original Long URL';
